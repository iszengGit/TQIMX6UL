void downlaod_uboot_2kpage(void) {
	nand_info_t *nand;
	size_t len,actual;
	int i,ret;
	unsigned long filesize,kloadaddr;
	struct mtd_oob_ops oob_opts;

	if(strcmp("TQIMX6ULL => ", CONFIG_SYS_PROMPT)==0)
	{
		run_command_arg("nand erase.part u-boot;");
//		run_command_args("nand erase.part u-boot-env");

		nand = &nand_info[nand_curr_device];
		oob_opts.mode=MTD_OPS_RAW;
		oob_opts.ooblen=sizeof(fcb_oob);
		oob_opts.len=sizeof(fcb_ctrl);
		oob_opts.datbuf=fcb_ctrl;
		oob_opts.oobbuf=fcb_oob;
		oob_opts.ooboffs=0;

		//printf("download oob \n");
		for (i = 0; i < 4; ++i) {
			ret = mtd_write_oob(nand,i*0x20000,&oob_opts);//each 128K
		}
		//printf("download fcb \n");
		for (;i < 8; ++i) {
			len = sizeof(dbbt_ctrl);
			nand_write_skip_bad(nand, i*0x20000, &len, &actual,0x20000,(unsigned char*)dbbt_ctrl ,WITH_WR_VERIFY);
			len = sizeof(push_down_dbbt);
			nand_write_skip_bad(nand, i*0x20000+0x2000, &len, &actual,0x20000,(unsigned char*)push_down_dbbt ,WITH_WR_VERIFY);
		}

	}

	printf("filesize = %s\n", getenv("filesize"));
	if((str2long(getenv("filesize"),&filesize)!=0) && (str2long(getenv("kloadaddr"),&kloadaddr)!=0))
		run_command_arg("nand write.e %x 0x100000 %x",kloadaddr-1024,filesize+1024);
}
